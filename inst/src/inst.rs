pub enum Inst {
    Nop,
    Left,
    Down,
    Up,
    Right,
    Add,
    Sub,
    Mul,
    Div,
}
impl Inst {
    pub fn new(key: char) -> Inst {
        use Inst::*;
        match key {
            '\n' => Nop,
            '!' => Nop,
            '"' => Nop,
            '#' => Nop,
            '$' => Nop,
            '%' => Nop,
            '&' => Nop,
            '\'' => Nop,
            '(' => Nop,
            ')' => Nop,
            '*' => Mul,
            '+' => Add,
            ',' => Nop,
            '-' => Sub,
            '.' => Nop,
            '/' => Div,
            '0' => Nop,
            '1' => Nop,
            '2' => Nop,
            '3' => Nop,
            '4' => Nop,
            '5' => Nop,
            '6' => Nop,
            '7' => Nop,
            '8' => Nop,
            '9' => Nop,
            ':' => Nop,
            ';' => Nop,
            '<' => Nop,
            '=' => Nop,
            '>' => Nop,
            '?' => Nop,
            '@' => Nop,
            'A'..='Z' => Nop,
            '[' => Nop,
            '\\' => Nop,
            ']' => Nop,
            '^' => Nop,
            '_' => Nop,
            '`' => Nop,
            'a' => Nop,
            'b' => Nop,
            'c' => Nop,
            'd' => Nop,
            'e' => Nop,
            'f' => Nop,
            'g' => Nop,
            'h' => Left,
            'i' => Nop,
            'j' => Down,
            'k' => Up,
            'l' => Right,
            'm' => Nop,
            'n' => Nop,
            'o' => Nop,
            'p' => Nop,
            'q' => Nop,
            'r' => Nop,
            's' => Nop,
            't' => Nop,
            'u' => Nop,
            'v' => Nop,
            'w' => Nop,
            'x' => Nop,
            'y' => Nop,
            'z' => Nop,
            '{' => Nop,
            '|' => Nop,
            '}' => Nop,
            '~' => Nop,
            _ => Nop,
        }
    }
}
